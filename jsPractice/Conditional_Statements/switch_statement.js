const day = "Monday";

switch (day) {
  case "Monday":
    console.log("Start of work week");
    break;
  case "Friday":
    console.log("TGIF!");
    break;
  default:
    console.log("Midweek");
}

// In switch  we use an case , break and default 
// case for use an statement 
// break for an break an stetment 
//default for use an default statement when the case is not match


